dia = int(input('Digite o dia de 1 a 7: '))

match dia:
    case 1: 
        print('Hojé é segunda-feira')
    case 2:
        print('Hoje é terça-feira')
    case 3:
        print('Hoje é quarta-feira')
    case 4:
        print('Hoje é quita-feira')
    case 5:
        print('Hoje é sexta-feira')
    case 6:
        print('Hoje é sábado')
    case 7:
        print('Hoje é domingo')